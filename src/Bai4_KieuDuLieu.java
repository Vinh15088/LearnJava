public class Bai4_KieuDuLieu {
    public static void main(String[] args) {
        // kiem tra kieu nguyen
        System.out.println("Min cua kieu Int: " + Integer.MIN_VALUE);
        System.out.println("Max cua kieu Int: " + Integer.MAX_VALUE);

        // kiem tra kieu byte
        System.out.println("Min cua kieu ByteL " + Byte.MIN_VALUE);
        System.out.println("Max cua kieu ByteL " + Byte.MAX_VALUE);
    }
}
